package aula09;

public class Conversor {
	double valorDolar, valorEuro;
	
	public double converterDolar(double valor) {
		return valor * valorDolar; //5.23
		
	}
	
	public double converterEuro(double valor) {
		return valor * valorEuro; //6.1
	}

}
